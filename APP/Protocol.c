unsigned short do_crc(u8 *pack, int num)
{
	unsigned char CRCcode_H = 0XFF;		// CRC High Init
  unsigned char CRCcode_L = 0XFF;		// CRC Low Init
  unsigned char index,i=0;			// index
  while (num--) {
        index = CRCcode_L ^ (pack[i++]);
        CRCcode_L = CRCcode_H ^ CRC_H[index];
        CRCcode_H = CRC_L[index];
    }
    return (CRCcode_L << 8 | CRCcode_H);	// MODBUS  
};







